package org.fabgas.utils;

public class HashMap<K,V> extends java.util.HashMap<K,V> implements Map<K,V> {
	public HashMap(){
		super();
	}
	public HashMap(int arg0){
		super(arg0);
	}
	public HashMap(java.util.Map<? extends K, ? extends V> arg0){
		super(arg0);
	}
	public HashMap(int arg0, float arg1){
		super(arg0, arg1);
	}
	private static final long serialVersionUID = 1L;
};
